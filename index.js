// Function to divide two numbers
const divide = (a, b) => {
    return a / b;
  };
  
  // Arrow function to square a number
  const square = (x) => {
    return x * x;
  };
  
  // Arrow function to add two numbers
  const add = (a, b) => {
    return a + b;
  };
  
  module.exports = {
    divide,
    square,
    add
  };
  